# looping on ruby
# for loop
puts "for loop:"
for i in 1..5
  puts "Iteration #{i}"
end

puts "*" * 10

# while loop
puts "while loop:"
j = 1
while j <= 5
  puts "Iteration #{j}"
  j += 1
end

puts "*" * 10

# until loop
puts "until loop:"
k = 1
until k > 5
  puts "Iteration #{k}"
  k += 1
end

puts "*" * 10
